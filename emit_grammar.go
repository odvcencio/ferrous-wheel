package ferrouswheel

import (
	"bytes"
	"fmt"
	"go/format"
	"strconv"
	"strings"
)

// EmitGrammarGo takes a Grammar IR and emits Go source code that reconstructs
// it using grammargen DSL calls. The output is a standalone Go file in the given
// package with a function of the given name that returns *Grammar.
func EmitGrammarGo(g *GrammarType, pkgName, funcName string) ([]byte, error) {
	var buf bytes.Buffer

	fmt.Fprintf(&buf, "package %s\n\n", pkgName)
	fmt.Fprintf(&buf, "// %s returns the %s grammar.\n", funcName, g.Name)
	fmt.Fprintf(&buf, "// Code generated by EmitGrammarGo. DO NOT EDIT.\n")
	fmt.Fprintf(&buf, "func %s() *Grammar {\n", funcName)
	fmt.Fprintf(&buf, "\tg := NewGrammar(%s)\n\n", emitGoString(g.Name))

	// Emit rules in order.
	for _, name := range g.RuleOrder {
		rule := g.Rules[name]
		fmt.Fprintf(&buf, "\tg.Define(%s,\n", emitGoString(name))
		emitRule(&buf, rule, 2)
		fmt.Fprintf(&buf, "\t)\n\n")
	}

	// Emit extras.
	if len(g.Extras) > 0 {
		fmt.Fprintf(&buf, "\tg.SetExtras(\n")
		for _, extra := range g.Extras {
			emitRule(&buf, extra, 2)
			fmt.Fprintf(&buf, ",\n")
		}
		fmt.Fprintf(&buf, "\t)\n\n")
	}

	// Emit conflicts.
	if len(g.Conflicts) > 0 {
		fmt.Fprintf(&buf, "\tg.SetConflicts(\n")
		for _, conflict := range g.Conflicts {
			fmt.Fprintf(&buf, "\t\t[]string{")
			for i, name := range conflict {
				if i > 0 {
					fmt.Fprintf(&buf, ", ")
				}
				fmt.Fprintf(&buf, "%s", emitGoString(name))
			}
			fmt.Fprintf(&buf, "},\n")
		}
		fmt.Fprintf(&buf, "\t)\n\n")
	}

	// Emit externals.
	if len(g.Externals) > 0 {
		fmt.Fprintf(&buf, "\tg.SetExternals(\n")
		for _, ext := range g.Externals {
			emitRule(&buf, ext, 2)
			fmt.Fprintf(&buf, ",\n")
		}
		fmt.Fprintf(&buf, "\t)\n\n")
	}

	// Emit inline.
	if len(g.Inline) > 0 {
		fmt.Fprintf(&buf, "\tg.SetInline(")
		for i, name := range g.Inline {
			if i > 0 {
				fmt.Fprintf(&buf, ", ")
			}
			fmt.Fprintf(&buf, "%s", emitGoString(name))
		}
		fmt.Fprintf(&buf, ")\n\n")
	}

	// Emit word.
	if g.Word != "" {
		fmt.Fprintf(&buf, "\tg.SetWord(%s)\n\n", emitGoString(g.Word))
	}

	// Emit supertypes.
	if len(g.Supertypes) > 0 {
		fmt.Fprintf(&buf, "\tg.SetSupertypes(")
		for i, name := range g.Supertypes {
			if i > 0 {
				fmt.Fprintf(&buf, ", ")
			}
			fmt.Fprintf(&buf, "%s", emitGoString(name))
		}
		fmt.Fprintf(&buf, ")\n\n")
	}

	// Emit EnableLRSplitting.
	if g.EnableLRSplitting {
		fmt.Fprintf(&buf, "\tg.EnableLRSplitting = true\n\n")
	}

	// Emit BinaryRepeatMode.
	if g.BinaryRepeatMode {
		fmt.Fprintf(&buf, "\tg.BinaryRepeatMode = true\n\n")
	}

	fmt.Fprintf(&buf, "\treturn g\n")
	fmt.Fprintf(&buf, "}\n")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Return unformatted source with error context for debugging.
		return buf.Bytes(), fmt.Errorf("gofmt: %w\n%s", err, buf.Bytes())
	}
	return formatted, nil
}

// emitRule writes the Go code for a single Rule node at the given indent level.
func emitRule(buf *bytes.Buffer, r *Rule, indent int) {
	tabs := strings.Repeat("\t", indent)

	if r == nil {
		fmt.Fprintf(buf, "%sBlank()", tabs)
		return
	}

	switch r.Kind {
	case RuleBlank:
		fmt.Fprintf(buf, "%sBlank()", tabs)

	case RuleString:
		fmt.Fprintf(buf, "%sStr(%s)", tabs, emitGoString(r.Value))

	case RulePattern:
		fmt.Fprintf(buf, "%sPat(%s)", tabs, emitGoPattern(r.Value))

	case RuleSymbol:
		fmt.Fprintf(buf, "%sSym(%s)", tabs, emitGoString(r.Value))

	case RuleSeq:
		fmt.Fprintf(buf, "%sSeq(\n", tabs)
		for _, child := range r.Children {
			emitRule(buf, child, indent+1)
			fmt.Fprintf(buf, ",\n")
		}
		fmt.Fprintf(buf, "%s)", tabs)

	case RuleChoice:
		fmt.Fprintf(buf, "%sChoice(\n", tabs)
		for _, child := range r.Children {
			emitRule(buf, child, indent+1)
			fmt.Fprintf(buf, ",\n")
		}
		fmt.Fprintf(buf, "%s)", tabs)

	case RuleRepeat:
		if len(r.Children) == 1 {
			fmt.Fprintf(buf, "%sRepeat(\n", tabs)
			emitRule(buf, r.Children[0], indent+1)
			fmt.Fprintf(buf, ",\n")
			fmt.Fprintf(buf, "%s)", tabs)
		}

	case RuleRepeat1:
		if len(r.Children) == 1 {
			fmt.Fprintf(buf, "%sRepeat1(\n", tabs)
			emitRule(buf, r.Children[0], indent+1)
			fmt.Fprintf(buf, ",\n")
			fmt.Fprintf(buf, "%s)", tabs)
		}

	case RuleOptional:
		if len(r.Children) == 1 {
			fmt.Fprintf(buf, "%sOptional(\n", tabs)
			emitRule(buf, r.Children[0], indent+1)
			fmt.Fprintf(buf, ",\n")
			fmt.Fprintf(buf, "%s)", tabs)
		}

	case RuleToken:
		if len(r.Children) == 1 {
			fmt.Fprintf(buf, "%sToken(\n", tabs)
			emitRule(buf, r.Children[0], indent+1)
			fmt.Fprintf(buf, ",\n")
			fmt.Fprintf(buf, "%s)", tabs)
		}

	case RuleImmToken:
		if len(r.Children) == 1 {
			fmt.Fprintf(buf, "%sImmToken(\n", tabs)
			emitRule(buf, r.Children[0], indent+1)
			fmt.Fprintf(buf, ",\n")
			fmt.Fprintf(buf, "%s)", tabs)
		}

	case RuleField:
		if len(r.Children) == 1 {
			fmt.Fprintf(buf, "%sField(%s,\n", tabs, emitGoString(r.Value))
			emitRule(buf, r.Children[0], indent+1)
			fmt.Fprintf(buf, ",\n")
			fmt.Fprintf(buf, "%s)", tabs)
		}

	case RulePrec:
		if len(r.Children) == 1 {
			fmt.Fprintf(buf, "%sPrec(%d,\n", tabs, r.Prec)
			emitRule(buf, r.Children[0], indent+1)
			fmt.Fprintf(buf, ",\n")
			fmt.Fprintf(buf, "%s)", tabs)
		}

	case RulePrecLeft:
		if len(r.Children) == 1 {
			fmt.Fprintf(buf, "%sPrecLeft(%d,\n", tabs, r.Prec)
			emitRule(buf, r.Children[0], indent+1)
			fmt.Fprintf(buf, ",\n")
			fmt.Fprintf(buf, "%s)", tabs)
		}

	case RulePrecRight:
		if len(r.Children) == 1 {
			fmt.Fprintf(buf, "%sPrecRight(%d,\n", tabs, r.Prec)
			emitRule(buf, r.Children[0], indent+1)
			fmt.Fprintf(buf, ",\n")
			fmt.Fprintf(buf, "%s)", tabs)
		}

	case RulePrecDynamic:
		if len(r.Children) == 1 {
			fmt.Fprintf(buf, "%sPrecDynamic(%d,\n", tabs, r.Prec)
			emitRule(buf, r.Children[0], indent+1)
			fmt.Fprintf(buf, ",\n")
			fmt.Fprintf(buf, "%s)", tabs)
		}

	case RuleAlias:
		if len(r.Children) == 1 {
			fmt.Fprintf(buf, "%sAlias(\n", tabs)
			emitRule(buf, r.Children[0], indent+1)
			fmt.Fprintf(buf, ",\n")
			fmt.Fprintf(buf, "%s\t%s, %t,\n", tabs, emitGoString(r.Value), r.Named)
			fmt.Fprintf(buf, "%s)", tabs)
		}
	}
}

// emitGoString returns a Go string literal for the given string.
func emitGoString(s string) string {
	return strconv.Quote(s)
}

// emitGoPattern returns a Go string literal for a regex pattern.
// Uses backtick quoting when the pattern contains no backticks or newlines.
func emitGoPattern(s string) string {
	if !strings.Contains(s, "`") && !strings.Contains(s, "\n") {
		return "`" + s + "`"
	}
	return strconv.Quote(s)
}
